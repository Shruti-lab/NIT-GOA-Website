var images = ['../images/pics_1.jpg', '../images/pics_2.png', '../images/pics_3.jpeg', '../images/pics_4.jpeg'];
var i = 0;
function change() {
    document.getElementById('picbg').style.backgroundImage = "url('" + images[i] + "')";
    i++;
    if (i == images.length) {
        i = 0;
    }
    setTimeout(change, 1000);
}
